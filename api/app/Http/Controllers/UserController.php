    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        /** @var User $location */
        $user = User::find($id);

        if($user) {
            $user->delete();

            return ["status" => true];
        }

        return $this->errorMsg();
    }
